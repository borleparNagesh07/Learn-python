# set is collection of non repetitive elements

s = set ()
s.add(1)
s.add(2)

# Properties
# Unordered: Sets do not maintain any order for the items.
# Mutable:You can add or remove items from a set.
# Unique Elements:Sets automatically ensure that all elements are unique (duplicates are removed).
# Not Indexed:Since sets are unordered, you cannot access items by an index.
# Elements Must be Hashable:Only immutable (hashable) objects can be added to a set.

